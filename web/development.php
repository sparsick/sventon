  <br/>
  <b>Contributing</b>
  <p>
  If you would like to contribute we are happy to accept bug reports, feature requests and patches!
  If you fix bugs and/or build extensions to sventon that you would like to contribute back to the project
  we will be happy to accept such contributions if we find them in line with where we think sventon is going.
  </p>
  <p>
  Please have a look at <a href="http://code.google.com/p/sventon/issues/list">
  http://code.google.com/p/sventon/issues/list</a> for some ideas of features/enhancements we are considering.
  </p>
  <p>
  The latest development sources can be accessed anonymously from
  <a href="http://sventon.googlecode.com/svn/trunk/">http://sventon.googlecode.com/svn/trunk/</a>.
  </p>
  <p>
  The best way of communicating is to use our
  <a href="http://groups.google.com/group/sventon-support/">Google group</a>. Patches can be
  <a href="http://groups.google.com/group/sventon-support/files?upload=1">uploaded to the group</a> or e-mailed directly
  to <a href="mailto:sventonproject@gmail.com">sventonproject@gmail.com</a>.
  </p>
  <p>
  When creating patches, use <code>svn diff</code> from the top of a sventon Subversion trunk working copy.
  The <a href="http://subversion.tigris.org/">Subversion</a> project has an
      <a href="http://subversion.apache.org/docs/community-guide/general.html#patches">excellent writeup about
  submitting patches</a>, much of what is mentioned in this text should be applied when submitting patches to sventon as
  well.
  </p>

